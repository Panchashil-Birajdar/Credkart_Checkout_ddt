import openpyxl


def ReadData(file, sheetname, rownum, colnum):
    workbook = openpyxl.load_workbook(file)             # for file
    sheet = workbook[sheetname]                         # for sheet
    return sheet.cell(row=rownum,column=colnum).value   # for row & column no

def WriteData(file, sheetname, rownum, colnum,data):
    workbook = openpyxl.load_workbook(file)
    sheet = workbook[sheetname]
    sheet.cell(row=rownum,column=colnum).value = data   # Entering data
    workbook.save(file)                                 # Save the file

def RowCount(file, sheetname):
    workbook = openpyxl.load_workbook(file)
    sheet = workbook[sheetname]
    return sheet.max_row







































































































































































































































































